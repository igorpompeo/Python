# crie um algoritmo que leia um número e
# mostre o seu dobro, triplo e raiz quadrada.
print("=== Desafio 06 ===")
número = int(input("Digite um número:"))
dobro = número * 2
triplo = número * 3
raiz_quadrada = número ** (1 / 2)
print(
    f"O dobro de {número} é {dobro},"
    f"o triplo é {triplo} e a raiz"
    f"quadrada é {raiz_quadrada:.2f}."
)
print("=== Fim do Desafio 06 ===")
