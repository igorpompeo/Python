# Desenvolva um programa que leia duas notas
# de um aluno, calcule e mostre a média.
print("=== Desafio 07 ===")
print("Média de notas")
n1 = float(input("Digite a primeira nota:"))  # nosec
n2 = float(input("Digite a segunda nota:"))  # nosec
